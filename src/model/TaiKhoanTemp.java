package model;

/**
 *
 * @author Vitaliy
 */
public class TaiKhoanTemp {
    String tenDangNhap;
    String matKhau;

    public TaiKhoanTemp() {
    }

    public TaiKhoanTemp(String tenDangNhap, String matKhau) {
        this.tenDangNhap = tenDangNhap;
        this.matKhau = matKhau;
    }

    public String getTenDangNhap() {
        return tenDangNhap;
    }

    public void setTenDangNhap(String tenDangNhap) {
        this.tenDangNhap = tenDangNhap;
    }

    public String getMatKhau() {
        return matKhau;
    }

    public void setMatKhau(String matKhau) {
        this.matKhau = matKhau;
    }
}
