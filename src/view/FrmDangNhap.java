    //  NÚT ĐĂNG NHẬP
    private void btnDangNhapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDangNhapActionPerformed
        String pass = String.valueOf(tfMatKhau.getPassword());
        if (String.valueOf(tfTenDangNhap.getText()).length() == 0) {
            JOptionPane.showMessageDialog(null, "Tên đăng nhập không được để trống!");
            tfTenDangNhap.requestFocus();
            return;
        } else
        if (String.valueOf(tfMatKhau.getPassword()).length() == 0) {
            JOptionPane.showMessageDialog(null, "Mật khẩu không được để trống!");
            tfMatKhau.requestFocus();
            return;
        } else
        if (tfTenDangNhap.getText().matches((".*\\s+.*"))){
            JOptionPane.showMessageDialog(null, "Tên đăng nhập không được chưa khoảng trắng");
            tfTenDangNhap.requestFocus();
            return;
        } else
        if(pass.matches(".*\\s+.*")){
            JOptionPane.showMessageDialog(null, "Mật khẩu không được chứa khoảng trắng");
            tfMatKhau.requestFocus();
            return;
        }
        if (ctl.KiemTraDangNhapAdmin(tfTenDangNhap.getText(), String.valueOf(tfMatKhau.getPassword()), this) == 1) {
            JOptionPane.showMessageDialog(null, "Đăng nhập quyền admin thành công!");
            FrmAdminTrangChu frmAdminTrangChu = new FrmAdminTrangChu();
            frmAdminTrangChu.setVisible(true);
            this.dispose();
            return;
        } else if (ctl.KiemTraDangNhapQuanLyVaChuCanHo(tfTenDangNhap.getText(), String.valueOf(tfMatKhau.getPassword()), this) == 2) {
            JOptionPane.showMessageDialog(null, "Đăng nhập quyền quản lý thành công!");
            FrmQuanLyTrangChu frmQuanLyTrangChu = new FrmQuanLyTrangChu();
            frmQuanLyTrangChu.setVisible(true);
            this.dispose();
            return;
        } else if ((ctl.KiemTraDangNhapQuanLyVaChuCanHo(tfTenDangNhap.getText(), String.valueOf(tfMatKhau.getPassword()), this) == 3)) {
            JOptionPane.showMessageDialog(null, "Đăng nhập quyền chủ căn hộ thành công");
            FrmChuCanHo frmChuCanHo = new FrmChuCanHo();
            frmChuCanHo.setVisible(true);
            this.dispose();
            return;
        } else {
            JOptionPane.showMessageDialog(null, "Sai tên đăng nhập hoặc mật khẩu");
        }

    }//GEN-LAST:event_btnDangNhapActionPerformed

