    //THÊM TÀI KHOẢN
    private void btnThemTaiKhoanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemTaiKhoanActionPerformed
        if (ctl.KiemTraTaiKhoanQuanLyTonTai(tfTenDangNhap.getText())==true || ctl.KiemTraTaiKhoanChuCanHoTonTai(tfTenDangNhap.getText())==true) {
            JOptionPane.showMessageDialog(null, "Tên đăng nhập bị trùng!");
            tfTenDangNhap.requestFocus();
            return;
       }

        int MaQuyen = 0;
        ComboboxValue TenQuyenDuocChon = (ComboboxValue) cbbQuyen.getSelectedItem();

        int SoLuongTaiKhoan = ctl.TongSoTaiKhoan();
        String strSLTK = Integer.toString(SoLuongTaiKhoan);
        String tenDangNhap = "tk" + strSLTK;
        String TenDangNhap = tfTenDangNhap.getText();
        String Ho = tfHo.getText();
        String Ten = tfTen.getText();
        String MatKhau = String.valueOf(tfMatKhau.getPassword());
        MaQuyen = Integer.parseInt(TenQuyenDuocChon.getKey());
        if (TenDangNhap.equals("")) {
            JOptionPane.showMessageDialog(null, "Nhập tên đăng nhập!");
            tfTenDangNhap.requestFocus();
            return;
        } else if (Ho.equals("")) {
            JOptionPane.showMessageDialog(null, "Họ chưa nhập");
            tfHo.requestFocus();
            return;
        } else if (Ten.equals("")) {
            JOptionPane.showMessageDialog(null, "Tên chưa nhập");
            tfTen.requestFocus();
            return;
        } else if (MatKhau.equals("")) {
            JOptionPane.showMessageDialog(null, "Mật khẩu chưa nhập");
            tfMatKhau.requestFocus();
            return;
        }

        if (MaQuyen == 2) {
            try {
                ctl.TaoTaiKhoan(tenDangNhap, String.valueOf(tfMatKhau.getPassword()), MaQuyen);
                ctl.TaoTaiKhoanQuanLy(tfTenDangNhap.getText(), tfHo.getText(), tfTen.getText(), tenDangNhap);
                JOptionPane.showMessageDialog(null, "Tạo tài khoản quản lý thành công");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Tạo tài khoản quản lý không thành công!");
            }
        }
        else if (MaQuyen == 3) {
            try {
                ctl.TaoTaiKhoan(tenDangNhap, String.valueOf(tfMatKhau.getPassword()), MaQuyen);
                ctl.TaoTaiKhoanChuCanHo(tfTenDangNhap.getText(), tfHo.getText(), tfTen.getText(), tenDangNhap);
                JOptionPane.showMessageDialog(null, "Tạo tài khoản chủ căn hộ thành công!");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Tạo tài khoản chủ căn hộ không thành công!");
            }
        }
    }//GEN-LAST:event_btnThemTaiKhoanActionPerformed

