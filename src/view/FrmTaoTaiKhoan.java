    public void DoQuyenVaoCombobox() {
        try {
            Connection connection = DBConnection.getConnection();
            String query = "SELECT * FROM Quyen";
            PreparedStatement ps = connection.prepareStatement(query);
            ResultSet rs = ps.executeQuery();
            DefaultComboBoxModel cbbModel = (DefaultComboBoxModel) cbbQuyen.getModel();
            cbbModel.removeAllElements();

            while (rs.next()) {
                String MaQuyen = rs.getString("MaQuyen");
                String TenQuyen = rs.getString("TenQuyen");

                if (MaQuyen.equals("1")) {
                    continue;
                }
                ComboboxValue mycbb = new ComboboxValue(MaQuyen, TenQuyen);
                cbbModel.addElement(mycbb);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

