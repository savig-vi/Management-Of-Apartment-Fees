    // CLICK ROW
    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        tblResult.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                int row = tblResult.getSelectedRow();
                String maDichVu = (String) tblResult.getValueAt(row, 0);
                String tenDichVu = (String) tblResult.getValueAt(row, 1);
                int donGia = (int) tblResult.getValueAt(row, 2);
                
                tfMaDichVu.setText(maDichVu);
                tfTenDichVu.setText(tenDichVu);
                tfDonGia.setText(String.valueOf(donGia));
            }
        });
        showDuLieu();
    }//GEN-LAST:event_formComponentShown

