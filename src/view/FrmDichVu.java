    public void showDuLieu(){
        try {
            tblResult.removeAll();
            String[] column = {"MÃ DỊCH VỤ", "TÊN DỊCH VỤ", "ĐƠN GIÁ"};
            DefaultTableModel model = new DefaultTableModel(column, 0);
            Connection connection = DBConnection.getConnection();
            String query = "SELECT * FROM DichVu";
            PreparedStatement ps = connection.prepareStatement(query);
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                Vector vector = new Vector();
                vector.add(rs.getString("MaDichVu"));
                vector.add(rs.getString("TenDichVu"));
                vector.add(rs.getInt("DonGia"));
                model.addRow(vector);
            }
            DBConnection.closeConnection(connection);
            tblResult.setModel(model);
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Lỗi không load được thông tin lên bảng");
        }
    }
